'''------------------------------------------------------
【程序设计】
---------------------------------------------------------
题目：循环从用户处获得一组数据(数字)，直到用户直接输入a退出，
      打印输出所有数据的和。本题不考虑输入异常情况。

输出实例
3
4
5
a
12
---------------------------------------------------------
注意：部分源程序给出如下。请勿改动其它函数中的任何内容，
      仅在函数的注释标志之间填入所编写的若干语句。
------------------------------------------------------'''

#**********Program**********
ls=[]
while True:
    lsint=input()
    if lsint=='a':
        print(sum(ls))
        break
    else:
        ls.append(eval(lsint))





#**********  End  **********





