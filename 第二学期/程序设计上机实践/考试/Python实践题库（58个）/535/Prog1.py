import time
timestr = "2020-10-10 10:10:10"
t = time.strptime(timestr, "%Y-%m-%d %H:%M:%S")#字符串转时间元组 format在后
print(time.strftime("%YY%mM%dD%HH%MM%SS", t))#时间元组转字符串 format在前

